class MovieData {
    constructor(name, poster, length, director, mainStars, rate, overview, genres, language, id) {
        this.name = name;
        this.poster = poster;
        this.length = length;
        this.director = director;
        this.mainStars = mainStars;
        this.rate = rate;
        this.overview = overview;
        this.genres = genres;
        this.language = language;
        this.id = id;
    }
}
export default MovieData

